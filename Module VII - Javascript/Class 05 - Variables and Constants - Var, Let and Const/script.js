// Declarando variável - VAR
var x;
console.log(x); // undefined

// Atribuindo valor na variável
x = 5;
console.log(x); // 5

// ------------------------------------------

// Declarando variável - LET
let x;
console.log(x); // undefined

// Atribuindo valor na variável
x = 5;
console.log(x); // 5

// ------------------------------------------

// Declarando constante - CONST
const x = 5; // PRECISA ser iniciada com algum valor.
console.log(x); // 5

// Não é permitido que uma CONSTANTE mude seu valor (SEJA REATRIBUÍDA)
x = 5; // error
